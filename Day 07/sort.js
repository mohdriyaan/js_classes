// sort() will sort the array in ascending or descending order
const a = [1,2,3,4,5,9,8,0,7]
console.log(a.sort((a,b)=>a-b)) //Ascending Order
console.log(a.sort((a,b)=>b-a)) //Descending Order
// Syntax : arr_name.sort((a,b)=>a-b) for ascending order
//          arr_name.sort((a,b)=>b-a) for descending order
